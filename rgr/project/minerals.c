/* Печатает строку, отцентрованную по ширине cols */
void print_centered(int row, int cols, const char *str, const char *color) {
    /* Вычисляем видимую длину строки (без escape-кодов) */
    int len = 0;
    int i = 0;
    while (str[i]) {
        if (str[i] == '\033') {
            /* пропускаем escape-последовательность */
            while (str[i] && str[i] != 'm') i++;
            if (str[i]) i++;
        } else {
            /* UTF-8: считаем символы */
            unsigned char c = (unsigned char)str[i];
            if (c < 0x80) { len++; i++; }
            else if ((c & 0xE0) == 0xC0) { len++; i += 2; }
            else if ((c & 0xF0) == 0xE0) { len++; i += 3; }
            else if ((c & 0xF8) == 0xF0) { len++; i += 4; }
            else { len++; i++; }
        }
    }
    int pad = (cols - len) / 2;
    if (pad < 0) pad = 0;
    move_to(row, 1);
    printf("%*s", pad, "");
    if (color) printf("%s", color);
    printf("%s", str);
    if (color) printf(CLR_RESET);
    fflush(stdout);
}

/* Длина UTF-8 строки в символах */
int utf8_strlen(const char *s) {
    int len = 0;
    while (*s) {
        unsigned char c = (unsigned char)*s;
        if (c < 0x80) { len++; s++; }
        else if ((c & 0xE0) == 0xC0) { len++; s += 2; }
        else if ((c & 0xF0) == 0xE0) { len++; s += 3; }
        else if ((c & 0xF8) == 0xF0) { len++; s += 4; }
        else { len++; s++; }
    }
    return len;
}

/* Рисует рамку */
void draw_box(int r1, int c1, int r2, int c2, const char *color) {
    if (color) printf("%s", color);
    move_to(r1, c1);
    printf("╔");
    for (int i = c1+1; i < c2; i++) printf("═");
    printf("╗");
    for (int r = r1+1; r < r2; r++) {
        move_to(r, c1); printf("║");
        move_to(r, c2); printf("║");
    }
    move_to(r2, c1);
    printf("╚");
    for (int i = c1+1; i < c2; i++) printf("═");
    printf("╝");
    if (color) printf(CLR_RESET);
    fflush(stdout);
}

/* ======================== СОХРАНЕНИЕ И ЗАГРУЗКА ======================== */
void save_db(void) {
    FILE *f = fopen(DATA_FILE, "wb");
    if (!f) return;
    fwrite(&db.count, sizeof(int), 1, f);
    fwrite(db.items, sizeof(Mineral), db.count, f);
    fclose(f);
}

void load_db(void);
void init_default_minerals(void);

void load_db(void) {
    FILE *f = fopen(DATA_FILE, "rb");
    if (!f) {
        init_default_minerals();
        save_db();
        return;
    }
    fread(&db.count, sizeof(int), 1, f);
    if (db.count < 0 || db.count > MAX_MINERALS) db.count = 0;
    fread(db.items, sizeof(Mineral), db.count, f);
    fclose(f);
}

/* ======================== СОРТИРОВКА ======================== */
int cmp_mineral(const void *a, const void *b) {
    return strcasecmp(((Mineral*)a)->name, ((Mineral*)b)->name);
}

void sort_db(void) {
    qsort(db.items, db.count, sizeof(Mineral), cmp_mineral);
}

/* ======================== НАЧАЛЬНЫЕ ДАННЫЕ ======================== */
void add_mineral(const char *name, const char *formula,
                 const char *cls, const char *color,
                 const char *luster, const char *hardness,
                 const char *density, const char *cleavage,
                 const char *system, const char *desc,
                 const char *disc) {
    if (db.count >= MAX_MINERALS) return;
    Mineral *m = &db.items[db.count++];
    strncpy(m->name,          name,      MAX_NAME-1);
    strncpy(m->formula,       formula,   MAX_FORMULA-1);
    strncpy(m->mineral_class, cls,       MAX_CLASS-1);
    strncpy(m->color,         color,     MAX_COLOR-1);
    strncpy(m->luster,        luster,    MAX_LUSTER-1);
    strncpy(m->hardness,      hardness,  MAX_HARDNESS-1);
    strncpy(m->density,       density,   MAX_DENSITY-1);
    strncpy(m->cleavage,      cleavage,  MAX_CLEAVAGE-1);
    strncpy(m->crystal_system,system,    MAX_SYSTEM-1);
    strncpy(m->description,   desc,      MAX_DESC-1);
    strncpy(m->discovery,     disc,      MAX_DISCOVERY-1);
}

