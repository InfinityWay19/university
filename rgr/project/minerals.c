/* ======================== УПРАВЛЕНИЕ ТЕРМИНАЛОМ ======================== */
void enable_raw_mode(void) {
    struct termios raw;
    tcgetattr(STDIN_FILENO, &orig_termios);
    raw = orig_termios;
    raw.c_lflag &= ~(ECHO | ICANON);
    raw.c_cc[VMIN] = 1;
    raw.c_cc[VTIME] = 0;
    tcsetattr(STDIN_FILENO, TCSAFLUSH, &raw);
}

void disable_raw_mode(void) {
    tcsetattr(STDIN_FILENO, TCSAFLUSH, &orig_termios);
}

TermSize get_terminal_size(void) {
    TermSize ts; struct winsize ws;
    if (ioctl(STDOUT_FILENO, TIOCGWINSZ, &ws)==-1) { ts.rows=24; ts.cols=80; }
    else { ts.rows=ws.ws_row; ts.cols=ws.ws_col; }
    return ts;
}

void clear_screen(void) {
    printf("\033[2J\033[H");
    fflush(stdout);
}

void hide_cursor(void) { printf("\033[?25l"); fflush(stdout); }
void show_cursor(void) { printf("\033[?25h"); fflush(stdout); }
void move_to(int row, int col) { printf("\033[%d;%dH", row, col); fflush(stdout); }

/* Чтение клавиши (возвращает: обычный символ или спец-коды) */
/* Спецкоды: 1000=UP, 1001=DOWN, 1002=LEFT, 1003=RIGHT, 13=ENTER, 27=ESC */
int read_key(void) {
    unsigned char c;
    if (read(STDIN_FILENO, &c, 1) != 1) return -1;
    if (c == 27) {
        unsigned char seq[3];
        if (read(STDIN_FILENO, &seq[0], 1) != 1) return KEY_ESC;
        if (read(STDIN_FILENO, &seq[1], 1) != 1) return KEY_ESC;
        if (seq[0] == '[') {
            switch (seq[1]) {
                case 'A': return KEY_UP;
                case 'B': return KEY_DOWN;
                case 'C': return KEY_RIGHT;
                case 'D': return KEY_LEFT;
            }
        }
        return KEY_ESC;
    }
    return (int)c;
}

